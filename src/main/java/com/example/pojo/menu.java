package com.example.pojo;

public class menu {

    String authorityTabName;
    String tabSizeDivision;
    String transitionType;
    String icon_code;



    public String getAuthorityTabName() {
        return authorityTabName;
    }

    public void setAuthorityTabName(String authorityTabName) {
        this.authorityTabName = authorityTabName;
    }

    public String getTabSizeDivision() {
        return tabSizeDivision;
    }

    public void setTabSizeDivision(String tabSizeDivision) {
        this.tabSizeDivision = tabSizeDivision;
    }

    public String getTransitionType() {
        return transitionType;
    }

    public void setTransitionType(String transitionType) {
        this.transitionType = transitionType;
    }

    public String getIcon_code() {
        return icon_code;
    }

    public void setIcon_code(String icon_code) {
        this.icon_code = icon_code;
    }
}
